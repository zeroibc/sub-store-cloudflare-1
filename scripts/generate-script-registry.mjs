import { existsSync, mkdirSync, readFileSync, realpathSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import { spawnSync } from "node:child_process";

const root = resolve(import.meta.dirname, "..");
const publicManifestPath = resolve(root, "config/script-plugins.json");
const localManifestPath = resolve(root, "config/script-plugins.local.json");
const outputPath = resolve(root, "cloudflare/src/generated/script-registry.ts");
const ID_PATTERN = /^[a-z0-9_-]{1,64}$/;
const MAX_SCRIPT_BYTES = 32 * 1024;
const MAX_SCRIPTS = 32;
const PARAMETER_TYPES = new Set(["string", "number", "boolean", "string-list"]);

const manifests = [loadManifest(publicManifestPath, false)];
if (existsSync(localManifestPath)) manifests.push(loadManifest(localManifestPath, true));
const entries = manifests.flatMap((manifest) => manifest.entries);
validateEntries(entries);

const source = [
  "// Generated by scripts/generate-script-registry.mjs. Do not edit.",
  "// @ts-nocheck -- personal compatibility scripts are JavaScript checked at generation time.",
  'import type { ScriptPlugin } from "../lib/scripts";',
  "",
  "export const SCRIPT_REGISTRY: readonly ScriptPlugin[] = [",
  ...entries.map(renderEntry),
  "];",
  "",
].join("\n");

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, source);
console.log(JSON.stringify({ output: relative(root, outputPath), scripts: entries.map((entry) => entry.id) }));

function loadManifest(path, local) {
  const parsed = JSON.parse(readFileSync(path, "utf8"));
  if (!Array.isArray(parsed.scripts)) throw new Error(`${relative(root, path)} must contain a scripts array`);
  return {
    entries: parsed.scripts.map((entry) => ({ ...entry, local, manifestPath: path })),
  };
}

function validateEntries(entries) {
  if (entries.length > MAX_SCRIPTS) throw new Error(`Script registry exceeds the ${MAX_SCRIPTS} script limit`);
  const ids = new Set();
  for (const entry of entries) {
    if (!ID_PATTERN.test(String(entry.id || ""))) throw new Error(`Invalid script id: ${entry.id || "<empty>"}`);
    if (ids.has(entry.id)) throw new Error(`Duplicate script id: ${entry.id}`);
    ids.add(entry.id);
    if (!String(entry.name || "").trim()) throw new Error(`Script ${entry.id} is missing a name`);
    if (!String(entry.description || "").trim()) throw new Error(`Script ${entry.id} is missing a description`);
    if (!['filter', 'operator'].includes(entry.kind)) throw new Error(`Script ${entry.id} kind must be filter or operator`);
    if (!entry.local && entry.compatibility !== "free") throw new Error(`Built-in script ${entry.id} must be Free-compatible`);
    if (entry.local) entry.compatibility = "personal";
    entry.parameters = Array.isArray(entry.parameters) ? entry.parameters : [];
    const keys = new Set();
    for (const parameter of entry.parameters) {
      if (!ID_PATTERN.test(String(parameter.key || ""))) throw new Error(`Script ${entry.id} has invalid parameter key`);
      if (keys.has(parameter.key)) throw new Error(`Script ${entry.id} has duplicate parameter: ${parameter.key}`);
      keys.add(parameter.key);
      if (!PARAMETER_TYPES.has(parameter.type)) throw new Error(`Script ${entry.id} parameter ${parameter.key} has unsupported type`);
    }

    const entryPath = resolve(dirname(entry.manifestPath), String(entry.entry || ""));
    if (!existsSync(entryPath)) throw new Error(`Script ${entry.id} entry does not exist: ${relative(root, entryPath)}`);
    const allowedRoot = resolve(root, entry.local ? "config/scripts.local" : "config/script-plugins");
    const realEntryPath = realpathSync(entryPath);
    const relativeEntryPath = relative(allowedRoot, realEntryPath);
    if (relativeEntryPath.startsWith("..") || relativeEntryPath === "") {
      throw new Error(`Script ${entry.id} entry must stay inside ${relative(root, allowedRoot)}`);
    }
    const code = readFileSync(realEntryPath, "utf8");
    if (Buffer.byteLength(code) > MAX_SCRIPT_BYTES) throw new Error(`Script ${entry.id} exceeds ${MAX_SCRIPT_BYTES} bytes`);
    if (/^\s*(?:import|export)\b/m.test(code)) throw new Error(`Script ${entry.id} cannot use import or export`);
    if (/\b(?:eval|require)\s*\(|\bnew\s+Function\s*\(/.test(code)) throw new Error(`Script ${entry.id} cannot use dynamic code loading`);
    if (!new RegExp(`\\bfunction\\s+${entry.kind}\\s*\\(`).test(code)) {
      throw new Error(`Script ${entry.id} must declare function ${entry.kind}(...)`);
    }
    const checked = spawnSync(process.execPath, ["--check", realEntryPath], { encoding: "utf8" });
    if (checked.status !== 0) throw new Error(`Script ${entry.id} has invalid JavaScript: ${checked.stderr.trim()}`);
    if (!entry.local && /\bfetch\s*\(|\$substore\s*\.\s*http|\$httpClient|\bsetTimeout\s*\(/.test(code)) {
      throw new Error(`Free-compatible script ${entry.id} cannot perform network requests or sleep`);
    }
    entry.code = code;
  }
}

function renderEntry(entry) {
  const metadata = {
    id: entry.id,
    name: entry.name,
    nameZh: entry.nameZh,
    description: entry.description,
    descriptionZh: entry.descriptionZh,
    kind: entry.kind,
    compatibility: entry.compatibility,
    parameters: entry.parameters,
  };
  return `  {\n    metadata: ${JSON.stringify(metadata, null, 2).replaceAll("\n", "\n    ")},\n    async run(proxies, runtime) {\n      const $arguments = runtime.arguments;\n      const $options = runtime.options;\n      const $substore = runtime.substore;\n      const ProxyUtils = runtime.proxyUtils;\n${indent(entry.code.trim(), 6)}\n      return await ${entry.kind}(proxies, runtime.targetPlatform, runtime.context);\n    },\n  },`;
}

function indent(value, spaces) {
  const prefix = " ".repeat(spaces);
  return value.split("\n").map((line) => `${prefix}${line}`).join("\n");
}
